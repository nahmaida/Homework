long getGreatestProduct(string num, int n) {
    long max = 1;
    for (int i = 0; i < num.size() - n; i++) {
        long product = 1;
        for (int j = 0; j < n; j++) {
            product *= num[i + j] - '0';
        }
        if (product > max) max = product;
    }
    return max;
}

